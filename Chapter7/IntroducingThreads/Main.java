/* Introducing Threads 
*
* A thread is the smallest unit of execution that can be scheduled by the operating system.

* A process (Java Program) is a group of associated threads that execute in the same, shared environment.

* A single-threaded-process is one that contains exactly one thread.
* A multi-threaded-process is one that contains one or more threads.

* By Shared Environment, we mean that the threads in the same process share the same memory space and can communicate with one another. Yes, you will finally see how static variables can be useful for performing complex, multi-threaded tasks!

* A task is a single unit of work performed by a thread.
*/