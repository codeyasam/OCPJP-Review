/* Design Patterns
*
* is an established general solution to a commonly occuring software development problem.
********** Creational Patterns ******************
* a type of software design pattern that manages the creation of objects within an application.

* The creational patterns simply apply a level of indirection to object creation by creating the object in some other class
* rather than creating the object directly in your application.

********** Level of Indirection *****************
* Is a general term for solving a software design problem by conceptually separating the task into multiple levels.
*/